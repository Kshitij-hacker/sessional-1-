#include <stdio.h>
int main()
{
    int a = 10, c;

    printf("c =%d \n", c = a);
    printf("c =%d \n", c += a);
    printf("c =%d \n", c -= a);
    printf("c =%d \n", c *= a);
    printf("c =%d \n", c /= a);
    printf("c =%d \n", c %= a);
}